#!/usr/bin/env bash
set -euo pipefail

FILE="env.sh"
USER_VAR="SF_USERNAME"
PASS_VAR="SF_PASSWORD"
SEARCH=""

usage() {
    cat <<EOF
Usage: $(basename "$0") [options] <search-string>

Search 1Password for a Login matching <search-string> and write its
username and password as environment variable exports.

Options:
  -f, --file <path>         Output file (default: env.sh)
  -u, --username <varname>  Username variable name (default: SF_USERNAME)
  -p, --password <varname>  Password variable name (default: SF_PASSWORD)
  -h, --help                Show this help
EOF
}

while [[ $# -gt 0 ]]; do
    case "$1" in
        -f|--file)     FILE="$2"; shift 2 ;;
        -u|--username) USER_VAR="$2"; shift 2 ;;
        -p|--password) PASS_VAR="$2"; shift 2 ;;
        -h|--help)     usage; exit 0 ;;
        --)            shift; SEARCH="${1:-}"; break ;;
        -*)            echo "Unknown option: $1" >&2; usage >&2; exit 2 ;;
        *)
            if [[ -z "$SEARCH" ]]; then SEARCH="$1"
            else echo "Unexpected argument: $1" >&2; exit 2
            fi
            shift ;;
    esac
done

if [[ -z "$SEARCH" ]]; then
    echo "Error: search string is required" >&2
    usage >&2
    exit 2
fi

for cmd in op jq gum; do
    if ! command -v "$cmd" >/dev/null 2>&1; then
        echo "Error: '$cmd' is required but not installed" >&2
        exit 1
    fi
done

# Query all Login items and filter by title (case-insensitive substring match).
matches_json="$(
    op item list --categories Login --format json \
        | jq --arg q "$SEARCH" '[.[] | select(.title | ascii_downcase | contains($q | ascii_downcase))]'
)"

count="$(jq 'length' <<<"$matches_json")"

if [[ "$count" -eq 0 ]]; then
    echo "No Login items matching '$SEARCH' found." >&2
    exit 1
fi

if [[ "$count" -eq 1 ]]; then
    item_id="$(jq -r '.[0].id' <<<"$matches_json")"
    item_title="$(jq -r '.[0].title' <<<"$matches_json")"
    echo "Found: $item_title"
else
    # Build "label<TAB>id" lines: gum shows the label and outputs the id on selection.
    item_id="$(
        jq -r '.[] | "\(.title)  (\(.additional_information // ""))\t\(.id)"' <<<"$matches_json" \
            | gum choose --header "Multiple Login items match '$SEARCH'. Select one:" --label-delimiter=$'\t'
    )"
    if [[ -z "$item_id" ]]; then
        echo "No selection made." >&2
        exit 1
    fi
    item_title="$(jq -r --arg id "$item_id" '.[] | select(.id==$id) | .title' <<<"$matches_json")"
fi

item_json="$(op item get "$item_id" --format json)"

username="$(jq -r '.fields[] | select(.id=="username" or .purpose=="USERNAME") | .value // empty' <<<"$item_json" | head -n1)"
password="$(jq -r '.fields[] | select(.id=="password" or .purpose=="PASSWORD") | .value // empty' <<<"$item_json" | head -n1)"

if [[ -z "$username" || -z "$password" ]]; then
    echo "Error: '$item_title' is missing a username or password field." >&2
    exit 1
fi

# Escape single quotes for safe single-quoted shell literals: ' -> '\''
esc() { printf "%s" "$1" | sed "s/'/'\\\\''/g"; }

umask 077
{
    echo "# Generated by $(basename "$0") on $(date)"
    echo "# Source 1Password item: $item_title"
    echo "export $USER_VAR='$(esc "$username")'"
    echo "export $PASS_VAR='$(esc "$password")'"
} > "$FILE"

echo "Wrote $USER_VAR and $PASS_VAR to $FILE"
